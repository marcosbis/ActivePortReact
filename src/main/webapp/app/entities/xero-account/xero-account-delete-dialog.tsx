import React, { useEffect } from 'react';
import { connect } from 'react-redux';
import { RouteComponentProps } from 'react-router-dom';
import { Modal, ModalHeader, ModalBody, ModalFooter, Button } from 'reactstrap';
import { Translate, ICrudGetAction, ICrudDeleteAction } from 'react-jhipster';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome';

import { IXeroAccount } from 'app/shared/model/xero-account.model';
import { IRootState } from 'app/shared/reducers';
import { getEntity, deleteEntity } from './xero-account.reducer';

export interface IXeroAccountDeleteDialogProps extends StateProps, DispatchProps, RouteComponentProps<{ id: string }> {}

export const XeroAccountDeleteDialog = (props: IXeroAccountDeleteDialogProps) => {
  useEffect(() => {
    props.getEntity(props.match.params.id);
  }, []);

  const handleClose = () => {
    props.history.push('/xero-account' + props.location.search);
  };

  useEffect(() => {
    if (props.updateSuccess) {
      handleClose();
    }
  }, [props.updateSuccess]);

  const confirmDelete = () => {
    props.deleteEntity(props.xeroAccountEntity.id);
  };

  const { xeroAccountEntity } = props;
  return (
    <Modal isOpen toggle={handleClose}>
      <ModalHeader toggle={handleClose}>
        <Translate contentKey="entity.delete.title">Confirm delete operation</Translate>
      </ModalHeader>
      <ModalBody id="activePortApp.xeroAccount.delete.question">
        <Translate contentKey="activePortApp.xeroAccount.delete.question" interpolate={{ id: xeroAccountEntity.id }}>
          Are you sure you want to delete this XeroAccount?
        </Translate>
      </ModalBody>
      <ModalFooter>
        <Button color="secondary" onClick={handleClose}>
          <FontAwesomeIcon icon="ban" />
          &nbsp;
          <Translate contentKey="entity.action.cancel">Cancel</Translate>
        </Button>
        <Button id="jhi-confirm-delete-xeroAccount" color="danger" onClick={confirmDelete}>
          <FontAwesomeIcon icon="trash" />
          &nbsp;
          <Translate contentKey="entity.action.delete">Delete</Translate>
        </Button>
      </ModalFooter>
    </Modal>
  );
};

const mapStateToProps = ({ xeroAccount }: IRootState) => ({
  xeroAccountEntity: xeroAccount.entity,
  updateSuccess: xeroAccount.updateSuccess,
});

const mapDispatchToProps = { getEntity, deleteEntity };

type StateProps = ReturnType<typeof mapStateToProps>;
type DispatchProps = typeof mapDispatchToProps;

export default connect(mapStateToProps, mapDispatchToProps)(XeroAccountDeleteDialog);
